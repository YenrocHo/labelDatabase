package com.fc.aden.vo;

public class ProductFoodStoreVO {

    private int product = 0;
    private int foodName = 0;
    private int store = 0;

    private int user = 0;
    private int stage = 0;
    public int getProduct() {
        return product;
    }

    public void setProduct(int product) {
        this.product = product;
    }

    public int getFoodName() {
        return foodName;
    }

    public void setFoodName(int foodName) {
        this.foodName = foodName;
    }

    public int getStore() {
        return store;
    }

    public void setStore(int store) {
        this.store = store;
    }

    public int getUser() {
        return user;
    }

    public void setUser(int user) {
        this.user = user;
    }

    public int getStage() {
        return stage;
    }

    public void setStage(int stage) {
        this.stage = stage;
    }
}
